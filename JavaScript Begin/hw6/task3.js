// *Напишите функцию pow(a, x), которая вернёт
// значение числа a, возведённого в степень x

{
    // Работает только для случаев, когда x представляет собой целое число не менее 1
    function pow(a, x) {
        let result = 1;

        for (let i = 1; i <= x; i++) {
            result *= a;
        }

        return result;
    }

    console.log(pow(9, 2));
}